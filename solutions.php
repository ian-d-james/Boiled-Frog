							<h3>Project Drawdown</h3>
							
							<p><a target="_blank" href="https://www.drawdown.org/">Project Drawdown</a> is a nonprofit organization and coalition of scholars, scientists, entrepreneurs, and advocates from across the 
							globe that is mapping, measuring, modeling, and communicating about a collective array of substantive solutions to global warming, with the goal of reaching drawdown.</p>
							<p>Drawdown is the point in time when the concentration of greenhouse gases in the Earth's atmosphere begins to decline on a year-to-year basis. Project Drawdown has developed realistic, solution-specific models, 
								technical assessments, and policy memos projecting the financial and climate impacts of existing solutions deployed at scale over the next thirty years.</p>
							<p>These are <b>proven</b> solutions that are already being implemented by humanity around the globe that promise enormous potential if scaled up over the next thirty years.</p>
							<p>Project Drawdown is the first effort to measure and project the collective impact of a broad range of solutions if implemented at scale. Rather than focusing on a single solution or sector of solutions, Project 
								Drawdown has done the math on what humanity is capable of achieving with the broad range of tools already in use around the globe.</p>
							<p>Solutions were evaluated using the following criteria:
								<ul style="overflow: hidden;">
									<li style="margin-left:30px;">Is the solution currently available and scaling?</li>
									<li style="margin-left:30px;">Is it economically viable? In other words, is there a business case?</li>
									<li style="margin-left:30px;">Does it have the potential to reduce GHGs in the atmosphere, either through avoided emissions or sequestration, by at least 50 million tons of greenhouse gasses over 30 years.</li>
									<li style="margin-left:30px;">Are there any negative results, such as pollution, reduced food security, land conversion, etc.? And if so, do the positive benefits outweigh the negatives?</li>
