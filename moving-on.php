							</div></p>

							<p>But is <a target="_blank" href="https://climate.nasa.gov/scientific-consensus/">consensus</a> a good basis for scientific decision making? A strong skeptic of global warming, author Michael Crichton offered the following opinion taken from his series 
							   of environmental and global warming <a target="_blank" href="../assets/content/crichton_3.pdf">essays</a> written in 2009:</p>

							<div style="font-style: italic; margin-left:30px; margin-right:30px;">"Let's be clear: the work of science has nothing whatever to do with consensus. Consensus is the business of politics.  Science, on the contrary, requires only one investigator who happens 
							to be right, which means that he or she has results that are 
							verifiable by reference to the real world.  In science, consensus is irrelevant. What is relevant is <b>reproducible results</b>. The greatest scientists in history are great precisely because they broke with the consensus."</div><br />

							<p>If consensus is irrelevant, what should be the foundation for our decision making? To answer this let's revisit the scientific method:</p>
							
							<ol style="margin-left:30px;">
								<li>Make an observation or observations.</li>
								<li>Ask questions about the observations and gather information.</li>
								<li>Form a hypothesis — a tentative description of what's been observed, and make <b>predictions based on that hypothesis</b>.</li>
								<li>Test the hypothesis and predictions in an experiment that can be <b>reproduced</b>.</li>
								<li>Analyze the data and draw conclusions; accept or reject the hypothesis or modify the hypothesis if necessary.</li>
								<li>Reproduce the experiment until there are no discrepancies between observations and theory.</li>
							</ol>

							<p><b>Predictability</b> is the ability to accurately define trends and future conditions such as atmospheric and oceanic temperature. Climate <a target="_blank" href="https://link.springer.com/article/10.1007/s00382-017-3939-8#Sec2">models</a> are inherently uncertain given that initial conditions can never be known perfectly and that the model is an imperfect representation 
							of global climate and its underlying physics. So just how accurate are the climate model predictions that are the foundation of the scientific publications above? Climate models published since 1973 have generally been quite skillful in projecting future warming. An overview of climate model predictive accuracy (both past and future) is provided on the 
							<a target="_blank" href="https://skepticalscience.com/how-well-have-models-predicted-gw.html">Skeptical Science</a> website. While some 
							temperature predictions were too low and some too high, they all show outcomes reasonably close to what has actually occurred.</p>

							<p><b>Reproducibility</b> is the ability to test a result using independent (different) methods and data processing. This term should not be confused with <b>replicability</b> which is the ability to rerun and validate the analysis using the same methods and data processing.</p>

							<div style="float:right;"><iframe style="margin-left:30px; margin-bottom:10px;" src="https://embed.ted.com/talks/gavin_schmidt_the_emergent_patterns_of_climate_change" width="500" height="280" frameborder="0" scrolling="no" ></iframe></div>
							
							<p>Climate model <a target="_blank" href="https://psmag.com/environment/what-a-reproducibility-crisis-committee-found-when-it-looked-at-climate-science">variability</a> isn't surprising given the enormous number of variables and interdependencies. Traditional reductionism can't be applied to climate modelling (see Gavin Schmidt's Ted talk), instead holistic climate models must contend with fourteen orders of 
							spatial and time magnitude. Models require massive and ever growing data sets 
							that can limit reproducibility. Even with good <a target="_blank" href="http://www.realclimate.org/index.php/archives/2018/05/transparency-in-climate-science/">data transparency</a>, resource and tool constraints (e.g., bandwidth and computational restrictions) 
							may preclude or hinder reproducing and verifying model results. Fortunately, there are a number of initiatives to enable climate scientists to better share, compare and analyze climate model results. One such initiative is the the 
							<a target="_blank" href="https://www.wcrp-climate.org/wgcm-cmip">coupled intercomparison project (CMIP)</a>.</p>

							<p>Climate model <a target="_blank" href="https://eos.org/opinions/climate-models-are-uncertain-but-we-can-do-something-about-it">predictive certainty</a> will continue to improve, although most climate scientists acknowledge that there are predictive limits. No matter how sophisticated our models become, there will always be an irreducible element of chaos in the earth’s climate system that no supercomputer can eliminate.</p>

							<p>There is much more evidence of global warming, of course, than that predicted by climate models. Examples include <a target="_blank" href="https://data.giss.nasa.gov/gistemp/">GISS Surface Temperature Analysis</a> 
							 which is the ongoing tracking of global surface temperature changes (both replicable and reproducible) and paleo-climate records such as those collected in projects like 
							 <a target="_blank" href="http://www.pages-igbp.org/ini/wg/2k-network/intro">PAGES2K</a>.</p>

							<p>This site does not attempt to address the full body of evidence for man-made climate change. There are many sites (see landing page) and <a href="references.php">references</a> providing compelling scientific evidence and 
							   expert testimony that man is driving and accelerating climate change. Instead, this site moves beyond the argument and targets individuals and organizations 
							   seeking strategies and actions to hold our educators, politicians, corporations and governments accountable for current and future climate policy. To hold
							   today's decision-makers accountable for the growing climate consequences that will be borne by our children and grandchildren. 
							</p>
