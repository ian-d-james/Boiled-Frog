							<p>As individuals, most of us understand what we can do to reduce emissions: save energy, use the car less, recycle more, 
							make better consumption choices, and engage in family planning. But what can our governments do? </p>
							<h3>Limiting Emissions - Carbon Tax</h3>
							<img style="float:left; solid gray; height:263px; width:341px; margin-bottom:8px; margin-right:24px;" src="./images/carbon tax2.jpg" />
							<p>Carbon tax is a form of pollution tax. It levies a fee on the production, distribution or use of fossil fuels based on how much 
							carbon their combustion emits. The government sets a price per ton on carbon, then translates it into a tax on electricity, natural 
							gas or oil. Because the tax makes using dirty fuels more­ expensive, it encourages utilities, businesses and individuals to reduce 
							consumption and increase energy efficiency. Carbon tax also makes alternative energy more cost-competitive with cheaper, 
							polluting fuels like coal, natural gas and oil.</p>

							<p>Carbon tax is based on the economic principle of negative externalities. Externalities are costs or benefits generated by the 
							production of goods and services. Negative externalities are costs that are not paid for. When utilities, businesses or homeowners
							 consume fossil fuels, they create pollution that has a societal cost; everyone suffers from the effects of pollution. Proponents 
							 of a carbon tax believe that the price of fossil fuels should account for these societal costs. More simply put -- if you're 
							 polluting to everyone else's detriment, you should have to pay for it.</p>
							<p>There are two main types of carbon pricing: emissions trading systems (ETS) and carbon taxes.</p>
							<p style="margin-left:30px;"><b>ETS</b>: sometimes referred to as a cap-and-trade system, it caps the total level of greenhouse gas emissions and lowers the cap over 
							time. Companies are allowed a limited, and falling, number of emissions permits. Those industries with low emissions are able to sell 
							their extra allowances to larger emitters. By creating supply and demand for emissions allowances, an ETS establishes a market price 
							for greenhouse gas emissions. The cap helps ensure that the required emission reductions will take place to keep the emitters (in aggregate) 
							within their pre-allocated carbon budget.</p>

							<p style="margin-left:30px;"><b>Carbon tax</b>: directly sets a price on carbon by defining a tax rate on greenhouse gas emissions or – more commonly – on the carbon content 
							of fossil fuels. It is different from an ETS in that the emission reduction outcome of a carbon tax is not pre-defined but the carbon price is.</p>

							<iframe style="float:right; margin-right:20px;" width="600" height="315" src="https://www.youtube.com/embed/xHIGuk27jVY" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>

							<p style="margin-top:15px;">Implementation of carbon pricing may be resisted if selectively adopted by the international market. In an open global economy, 
							adopting companies and countries risk being put in a pricing and competitive disadvantage. The World Bank's <a target="_blank" href="https://carbonpricingdashboard.worldbank.org/map_data">carbon 
