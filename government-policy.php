							<h3>Transition to Small Agricultural Producers</h3>

							<p>According to the Food and Agriculture Organization of the United Nations, the meat industry is responsible for 15 - 18 percent of all greenhouse 
							   gas emissions, exceeding even those of the transportation sector. In addition, it is the most significant source of water use and contamination in 
							   the world. Today, 80 percent of all agricultural production goes toward feeding animals not people. The expansion of land for livestock, and the 
							   crops to feed them, is the most significant cause of deforestation in the Amazon.</p>
							<p>Governments should increasingly support small regional producers who, unlike the large factory farms, employ more sustainable practices including 
							   land management and restoration.</p>

							<h3>Promote Renewable (Green) Energy</h3>
							<img style="float:right; solid gray; height:608px; width:367px; margin-bottom:8px; margin-left:24px;" src="./images/green-energy.jpg" />
							
							<p>According to the Global Energy Network Institute, the following government policies prove most effective for promoting renewable energy:</p>
							<ul style="width: auto; overflow: hidden;">
									<li><b>Performance standards</b>: For example, fuel efficiency standards for vehicles and buildings, including lighting, windows, 
									ventilation, heating and cooling systems.</li>
									<li><b>Fiscal instruments</b>: Some countries offer tax exemptions or tax breaks for appliances and energy efficiency improvements.</li>
									<li><b>Targets and timetables</b>: For example, setting renewable portfolio standards (RPS), mandating that a percentage of all energy  
									purchases be derived from renewable resources</li>
									<li><b>Technology performance standards</b>: For example, vehicle pollution standards and energy star</li>
									<li><b>Taxes and fees</b>: For example, higher taxes on fossil-fuel energy sources thereby increasing renewable energy competiveness</li>
									<li><b>Tariffs</b>: For example, private energy producer "feed-in" tariffs, establishing predictable market pricing for smaller energy producers</li>
									<li><b>Subsidy reform</b>: For example, reducing subsidies for the fossil-fuel industries</li>
									<li><b>Trade policies</b>: For eaxmple, cutting tariffs on green goods such as solar panels, wind turbines, and energy-efficient light bulbs can help ensure access to the best technologies available globally.</li>
									<li><b>Venture capital provision</b>: For example, renewable enery equity funding for renewable energy start-up companies</li>
									<li><b>Technology procurement</b>: For example, government guarantees for market demand</li>

								</ul>

							<h3>Reduce Short-Lived Climate Pollutants</h3>

							<p>Unlike CO2, which may require millennia for reabsorption, there are many contaminants that contribute to climate change that only last a few days 
							   or years in the atmosphere. They’re known as short-lived climate pollutants, and they’re responsible for 30 to 45 percent of the emissions that 
							   cause global warming. These pollutants include black carbon (soot), methane, ozone, and the hydro fluorocarbons found in refrigerants. Their 
							   effective control, through national policies and regulations, could accelerate the fight against climate change in the short term.</p>

							
							<img style="float:left; border:1px solid gray; height:263px; width:300px; margin-bottom:8px; margin-right:24px;" src="./images/un-logo.png" />
							<h3>International Agreements</h3>

							<p>The <a target="_blank" href="https://www.consilium.europa.eu/en/policies/climate-change/international-agreements-climate-action/">United Nations 
							Framework Convention on Climate Change (UNFCCC)</a> is the main international agreement on climate action. It was one of three conventions adopted 
							at the Rio Earth Summit in 1992. To date, it has been ratified by 195 countries. It started as a way for countries to work together to limit global 
							temperature increases and climate change, and to cope with their impacts.</p>

							<h3>Kyoto Protocol</h3>

							<p>In the mid 1990s, the UNFCCC signatories realised that stronger provisions were needed to reduce emissions. In 1997, they agreed the Kyoto 
							   Protocol, which introduced legally binding emission reduction targets for developed countries.</p>
							
							<p>The second commitment period of the Kyoto Protocol began on 1 January 2013 and will end in 2020. 38 developed countries, including the EU and 
							   its 28 member states, are participating. This second period is covered by the Doha amendment, under which participating countries have committed 
							   to reducing emissions by at least 18% below 1990 levels. The EU has committed to reducing emissions in this period to 20% below 1990 levels.</p>

							<p>The main weakness of the Kyoto Protocol is that it only requires developed countries to take action. As the United States has never signed up to 
							   the Kyoto Protocol, Canada pulled out before the end of the first commitment period and Russia, Japan and New Zealand are not taking part in the 
							   second commitment period, it also only now applies to around 14% of the world's emissions. However, more than 70 developing and developed countries 
							   have made various non-binding commitments to reduce or limit their greenhouse gas emissions.</p>

							<h3>Paris Agreement</h3>

							<p>At the Paris climate conference (COP21) in December 2015, 195 countries adopted the first-ever universal, legally binding global climate deal.
							   The agreement sets out a global action plan to put the world on track to avoid dangerous climate change by limiting global warming to well 
							    below 2°C and pursuing efforts to limit it to 1.5°C. Governments agreed:</p>
							<ul style="margin-left:30px;">
								<li>to a long-term goal of keeping the increase in global average temperature to well below 2°C above pre-industrial levels</li>
								<li>to aim to limit the increase to 1.5°C, since this would significantly reduce risks and the impacts of climate change</li>
								<li>on the need for global emissions to peak as soon as possible, recognising that this will take longer for developing countries</li>
								<li>to undertake rapid reductions thereafter in accordance with the best available science</li>
								<li>to come together every 5 years to set more ambitious targets as required by science</li>
								<li>to report to each other and the public on how well they are doing to implement their targets</li>
								<li>to track progress towards the long-term goal through a robust transparency and accountability system</li>
								<li>to strengthen societies' ability to deal with the impacts of climate change</li>
								<li>to provide continued and enhanced international support for adaptation to developing countries</li>
							</ul>

							<p>On June 1, 2017, United States President Donald Trump announced that the U.S. would cease all participation in the 2015 Paris 
							   Agreement on climate change mitigation. Trump stated that "The Paris accord will undermine the U.S. economy," and "puts the U.S. at a permanent disadvantage. 
							   In accordance with Article 28 of the Paris Agreement, the earliest possible effective withdrawal date by the United States cannot be before November 4, 2020.</p>

							<h3>United States - The Green Deal</h3>
							<img style="float:left; border:1px solid gray; height:342px; width:263px; margin-bottom:8px; margin-right:24px;" src="./images/green-deal.png" />
							
							
							<p>Introduced by Sen. Ed Markey (D-MA) and Rep. Alexandrai Ocasio-Cortez (D-NY), the <b>Green New Deal</b> is not just another climate policy. It recognizes the requirement to mobilize and address climate change and its many interconnected economic, social, political and ecological issues. It argues that we no longer have the luxury of time to 
							implement incremental or surgical climate policy changes - that opportunity has come and gone. Instead, the crisis demands our collective resolve to adopt timely and comprehensive policy change. Mass mobilization is required to avoid imminent ecological and economic disaster.  
							
							<p>Unfortunately, the policy may prove too broad as it seeks to address both climate change and economic inequality. Its socialist theme will likely alienate many who would otherwise support climate change policies. Nonetheless, the green deal 
							serves as an important wake-up call, recognizing the peril of status quo. It proposes a much broader scope and scale of policy change to address our ever accelerating ecological crisis.</p>

